package com.cn.yblog.util;

import java.io.Closeable;
import java.io.IOException;

/**
 * description: IO工具类
 *
 * @author yan.w.s@qq.com
 * <p>time: 2022/12/25
 * <p>version: 1.0
 * <p>update: none
 */
public class IOUtil {
    private IOUtil() {

    }

    public static void close(Closeable... closeables) {
        for (Closeable c : closeables) {
            if (c != null) {
                try {
                    c.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }
}
